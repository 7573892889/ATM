    private void DisplyTtr(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            cn = DriverManager.getConnection("jdbc:mysql://localhost:3306/atm", "root", "");
            String Query = "SELECT * FROM `transactiontb` where Account_number ='"+myaccount+"'";
            st=cn.createStatement();
            rs=st.executeQuery(Query);
            ResultSetMetaData rd=(ResultSetMetaData) rs.getMetaData();
            int n=((DefaultTableModel) rd).getColumnCount();
            DefaultTableModel dtm=(DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while(rs.next()){
                Vector v=new Vector();
                for(int i=0;i<n;i++){
                    v.add(rs.getString("Account_number"));
                    v.add(rs.getString("Balance"));
                    v.add(rs.getString("Date"));
                    v.add(rs.getString("Status"));
                }
                dtm.addRow(v);
            }
            
            
        }catch(Exception ex){
            JOptionPane.showMessageDialog(this,ex);
        }
    }
